    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // Este boton crearar los cafes con todos los ingredientes extras seleccionados
        // Existen 7 combinaciones posibles creo xD
        // Cafes con un ingrediente extra

         ingredientes = "Ingredientes : ";

        if (oreo == true && cajeta == false && panna == false) {
            Cafe cafe_o = new Oreo(base);
            ingredientes += cafe_o.ingredientes();
            costo_decorado = cafe_o.precio();
        }
        if (panna == true && oreo == false && cajeta == false) {
            Cafe cafe_p = new Panna(base);
            System.out.println(cafe_p.ingredientes());
            ingredientes += cafe_p.ingredientes();
            costo_decorado = cafe_p.precio();
        }
        if (cajeta == true && panna == false && oreo == false) {
            Cafe cafe_caj = new Cajeta(base);
            System.out.println(cafe_caj.ingredientes());
            ingredientes += cafe_caj.ingredientes();
            costo_decorado = cafe_caj.precio();
        }
        // Cafe con todos los ingredientes extras
        if (oreo == true && cajeta == true && panna == true) {
            Cafe cafe_todos = new Oreo(new Cajeta(new Panna(base)));
            System.out.println(cafe_todos.ingredientes());
            ingredientes += cafe_todos.ingredientes();
            costo_decorado = cafe_todos.precio();
        }
        // Cafe con oreo y cajeta
        if (oreo == true && cajeta == true && panna == false) {
            Cafe cafe_oc = new Oreo(new Cajeta(base));
            ingredientes += cafe_oc.ingredientes();
            costo_decorado = cafe_oc.precio();
        }
        // Cafe con oreo y panna
        if (oreo == true && panna == true && cajeta == false) {
            Cafe cafe_op = new Oreo(new Panna(base));
            ingredientes += cafe_op.ingredientes();
            costo_decorado = cafe_op.precio();
        }
        // Cafe con cajeta y panna
        if (cajeta == true && panna == true && oreo == false) {
            Cafe cafe_cp = new Cajeta(new Panna(base));
            ingredientes += cafe_cp.ingredientes();
            costo_decorado = cafe_cp.precio();
        }

        // Cafe sin nada pff
        if (cajeta == false && panna == false && oreo == false) {
            Cafe cafe_solo = base;
            ingredientes += cafe_solo.ingredientes();
            costo_decorado = cafe_solo.precio();
        }

        etiIngredientes.setText(ingredientes);
        etiPrecio_Decorado.setText("$ " + costo_decorado);
        txtTotalPagar.setText("$ " + costo_decorado * obtenCantidad());
        total_pagar = costo_decorado * obtenCantidad();

        cambiosMedicion();

    }//GEN-LAST:event_btnCrearActionPerformed

