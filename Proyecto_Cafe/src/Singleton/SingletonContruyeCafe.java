    private int obtenCantidad() {
        int cantidad = 0;
        if (!txtCantidad.getText().isEmpty()) {
            cantidad = Integer.parseInt(txtCantidad.getText());
        } else {
            JOptionPane.showMessageDialog(rootPane, "Ingrese Cantidad de cafee a comprar");
        }
        return cantidad;
    }

